#include <stdio.h>

int main()
{
    int n,m,i,j;
    printf("n = ");
    scanf("%d",&n);
    m = (n+1)/2;
    for (i=0; i<m; i++) {
        for (j=0; j<(m-1-i); j++) {
            printf("-");
        }
        for (j=0; j<(2*i+1); j++) {
            printf("*");
        }
        for (j=0; j<(m-1-i); j++) {
            printf("-");
        }
        printf("\n");
    }
    return 0;
}


