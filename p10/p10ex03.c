#include <stdio.h>
#include <stdlib.h>

#define swap(type, x, y) do { type t = y; y = x; x = t; } while (0)

typedef struct {
	int x;
	int y;
} move_t;

typedef struct{
	int move;
	int canGoNum;
} go_t;

move_t move[8]={
	{2,1},{1,2},{-1,2},{-2,1},
	{-2,-1},{-1,-2},{1,-2},{2,-1}
};
int board[34][34];
int counter;
int found=0;
int re=0;

void printBoard(int n)
{
	int i,j;
	printf("Tour #%d is found.\n",++found);
	for (i=2; i<(n+2); i++) {
		for (j=2; j<(n+2); j++) {
			printf("%4d",board[i][j]);
		}
		printf("\n");
	}
	if (found==3) re = 1;
}

void initBoard(int n)
{
	int i,j;
	for (i=0; i<(n+4); i++) {
		for (j=0; j<(n+4); j++) {
			board[i][j]=-1;
		}
	}
	for (i=2; i<(n+2); i++) {
		for (j=2; j<(n+2); j++) {
			board[i][j]=0;
		}
	}
}

int canGoNum(int x,int y)
{
	int i,counter=0,nextx,nexty;

	for(i=0;i<8;i++){
		nextx=x+move[i].x;
		nexty=y+move[i].y;
		if(board[nexty][nextx]==0) counter++;
	}
	return counter;
}

void knighttour(int x,int y,int n)
{
	int nextx,nexty,i,j,tmp;
	go_t go[8];
	for(i=0;i<8;i++){
		go[i].move=-1;
		go[i].canGoNum=8;
	}
	for(i=0;i<8;i++){
		nextx=x+move[i].x;
		nexty=y+move[i].y;
		if(board[nexty][nextx]==0){
			tmp=canGoNum(nextx,nexty);
			go[i].canGoNum=tmp;
			go[i].move=i;
		}
	}
	for(i=0;i<8;i++){
		for(j=8-1;j>i;j--){
			if(go[j].canGoNum<go[j-1].canGoNum || go[j].canGoNum==0){
				swap(go_t,go[j-1],go[j]);
			}
		}
	}
	
	for(i=0;i<8;i++){
		if(go[i].move!=-1){
			nextx=x+move[go[i].move].x;
			nexty=y+move[go[i].move].y;
			board[nexty][nextx]=++counter;
			if(counter==n*n){
				printBoard(n);
				if (re == 1) return;
			}else{
				knighttour(nextx,nexty,n);
				if (re == 1) return;
			}
			board[nexty][nextx]=0;
			--counter;
		}
	}
	return;
}

int main()
{
	int x,y,n,i;
	for (n=5;n<=30;n++){
		initBoard(n);
		counter=1;
		x=2;
		y=2;
		board[y][x]=counter;
		printf("\n%d * %d Tour start!\n",n,n);
		knighttour(x,y,n);
		re = 0;
		found = 0;
		printf("%d * %d Tour finish!\n",n,n);
	}
	return 0;
}


