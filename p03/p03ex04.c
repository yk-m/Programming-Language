int main()
{
	int n,count;
	printf("n = ");
	scanf("%d",&n);
	count = 1;
	while (count<=n) {
		printf("*");
		if (count%5==0){
			printf("-");
		};
		count++;
	};
	printf("\n");
	return 0;
}


