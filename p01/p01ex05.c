int main()
{
    double ondo,onsoku;
    printf("温度[℃] = ");
    scanf("%lf",&ondo);
    
    onsoku = 331.5+0.6*ondo;
    printf("音速[m/s] = %.1lf\n",onsoku);
    return 0;
}


